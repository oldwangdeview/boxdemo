package com.oldwang.boxdemo.bean;

public class MasterData {

    private String headLogo;
    private String newsMasterId;
    private String newsMasterName;
    private String isPraise;
    private String newsMasterDetail;
    private String newsMasterPraiseCount;

    public String getIsPraise() {
        return isPraise;
    }

    public void setIsPraise(String isPraise) {
        this.isPraise = isPraise;
    }

    public String getNewsMasterDetail() {
        return newsMasterDetail;
    }

    public void setNewsMasterDetail(String newsMasterDetail) {
        this.newsMasterDetail = newsMasterDetail;
    }

    public String getNewsMasterPraiseCount() {
        return newsMasterPraiseCount;
    }

    public void setNewsMasterPraiseCount(String newsMasterPraiseCount) {
        this.newsMasterPraiseCount = newsMasterPraiseCount;
    }

    public String getHeadLogo() {
        return headLogo;
    }

    public void setHeadLogo(String headLogo) {
        this.headLogo = headLogo;
    }

    public String getNewsMasterId() {
        return newsMasterId;
    }

    public void setNewsMasterId(String newsMasterId) {
        this.newsMasterId = newsMasterId;
    }

    public String getNewsMasterName() {
        return newsMasterName;
    }

    public void setNewsMasterName(String newsMasterName) {
        this.newsMasterName = newsMasterName;
    }
}
